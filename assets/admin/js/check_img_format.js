
var fl = document.getElementById('blah1');

fl.onchange = function(e){
  var ext = this.value.match(/\.(.+)$/)[1];
  var format = ext.toLowerCase();
  switch(format)
  {
    case 'jpg':
    case 'jpeg':
    case 'png':
    case 'tif':
      break;
    default:
      alert('فرمت عکس باید jpg یا jpeg یا tif یا png باشد.  ');
      this.value='';
  }
};
